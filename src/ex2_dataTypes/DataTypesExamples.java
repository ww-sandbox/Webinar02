package ex2_dataTypes;

public class DataTypesExamples {

    public static void main(String[] args) {

        System.out.println("Tekst do wyświetlenia"); //podstawowy sposób wyświetlania tekstu w Java
        //tekst wyświetlany jest w wyjściu standardowym - najczęściej jest to konsola (terminal)

        int a = 1; //zmienna typu integer (liczba całkowita)

        double b = 2.5; //liczba zmiennoprzecinkowa
        float c = 3.2f; //liczba zmiennoprzecinkowa
        //double posiada precyzję 15-16 miejsc po przecinku(i rozmiar) 64bit, float 6-7 miejsc po przecinku i 32bit

        boolean d = true; //zmienna typu logicznego zaincjalizowana wartością true
        boolean e = false; //zbmienna typu logicznego zainicjalizowana wartością false
        //są to jedyne wartości jakie może przyjąć zmienna typu boolean

        char ch = 'a'; //zmienna typu znak
        char ch2 = 97; //sprawdź jaki znak zostanie wyświetlony przy pomocy println

        //Zadanie stwórz nowe zmienne każdego typu i wyświetl je w konsoli



    }
}
